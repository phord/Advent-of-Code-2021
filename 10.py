def parse(input):
    lines = [x for x in input.splitlines() if x]
    return lines

def chunk(line, pos):
    open = "([{<"
    close = ")]}>"
    x = open.find(line[pos])
    if x == -1:
        return (pos, "")
    pos += 1
    autocomp = ""
    while pos < len(line):
        newpos, autocomp = chunk(line, pos)
        if newpos < 0:
            return (newpos, autocomp+close[x])
        if newpos == pos:
            break
        pos = newpos
    if pos == len(line):
        return pos, autocomp+close[x]
    if line[pos] == close[x]:
        return (pos+1, "")
    # mismatch
    return (-pos, "")

def split_chunk(line):
    pos, comp = chunk(line, 0)

    points = {
        ')' : 3,
        ']' : 57,
        '}' : 1197,
        '>' : 25137,
    }

    if pos < 0:
        print("  >>> CORRUPT")
        return (points[line[-pos]], 0, "", line)

    score = 0
    if comp:
        print(f"  Autocomp: {comp}")
        points = {
            ')' : 1,
            ']' : 2,
            '}' : 3,
            '>' : 4,
        }
        for ch in comp:
            score *= 5
            score += points[ch]
    return (0, score, line[:pos], line[pos:])

def part1(input, part2=False):
    score = 0
    for line in input:
        print(line)
        while line:
            points, xx, chunk, line = split_chunk(line)
            score += points
            if points:
                print(f"Failed at {line} for {points} points")
                break
            print(" ", chunk)
        if not line:
            print("------ GOOD")
    return score


